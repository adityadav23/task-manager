const mongoose = require('mongoose');

const taskSchema = new mongoose.Schema({
    title: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    status: {
        type: String,
        enum: ['pending', 'in-progress', 'completed'],
        default: 'pending'
    },
    priority: {
        type: String,
        enum: ['low', 'medium', 'high'],
        default: 'medium'
    },
    dueDate: {
        type: Date,
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    },
    updatedAt: {
        type: Date,
        default: Date.now
    },
    createdBy: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true
    },
    assignedTo: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User'
    }
});

// Indexes
taskSchema.index({ status: 1 }); // Index on status
taskSchema.index({ dueDate: 1 }); // Index on dueDate
taskSchema.index({ createdBy: 1 }); // Index on createdBy
taskSchema.index({ assignedTo: 1 }); // Index on assignedTo

const Task = mongoose.model('Task', taskSchema);

module.exports = Task;
